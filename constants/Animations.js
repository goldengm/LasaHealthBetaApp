export function getAnimationFromPath(path) {
  const pathParts = path.split(".");
  if (!(pathParts == null) && pathParts.length == 4) {
    return JSON.parse(
      JSON.stringify(Animations[pathParts[1]][pathParts[2]][pathParts[3]])
    );
  } else {
    return null;
  }
}

